	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
		$data = Input::json()->all();
		$thread = new Thread;
		$thread->x = $data["x"];
		$thread->y = $data["y"];
		$thread->image_id = $data["image_id"];
		$thread->save(); 

		foreach($data["comments"] as $comment) {
			$comment = new Comment($comment);
			$thread->comments()->save($comment);
		}
		
		return Response::json($thread->load("comments"), 201);
	}

